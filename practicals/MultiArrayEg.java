public class MultiArrayEg {
	public static void main(String[] args) {
				int arr1[][]={{9,8,7},{4,2,8},{4,5,2}};//declare &initialize
		//print
		for(int i=0;i<3;i++){//rows
			for(int j=0;j<3;j++){//columns
				System.out.print(arr1[i][j] + " ");
			}
			System.out.println();
		}

	}

}
